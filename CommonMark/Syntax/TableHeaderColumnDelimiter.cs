namespace CommonMark.Syntax
{
    /// <summary>
    /// Defines the delimeter used in the source for header columns.
    /// </summary>
    public enum TableHeaderColumnDelimiter
    {
        /// <summary>
        /// The table uses pipe characters as header column delimiters.
        /// </summary>
        Pipe = 0,

        /// <summary>
        /// The table uses plus characters as header column delimiters.
        /// </summary>
        Plus,
    }
}
